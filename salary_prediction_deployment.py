import streamlit as st
import pandas as pd
import joblib

model = joblib.load("salary_prediction_rfr_model.pkl")
encoder = joblib.load("label_encoder_salary.pkl")

st.title("Salary Prediction Model")

age = st.number_input("Enter your age", 18, 65)
gender = st.selectbox("Select Gender", encoder["Gender"].classes_)
education = st.selectbox("Select Education", encoder["Education Level"].classes_)
job = st.selectbox("Select Job Title", encoder["Job Title"].classes_)

# Encode values
gender = encoder["Gender"].transform([gender])[0]
education = encoder["Education Level"].transform([education])[0]
job = encoder["Job Title"].transform([job])[0]

# Create dataframe with SAME column names as training
df = pd.DataFrame([[age, gender, education, job]],
columns=['Age','Gender','Education Level','Job Title'])

if st.button("Predict Salary"):
    prediction = model.predict(df)
    st.success(f"Predicted Salary: {prediction[0]}")
